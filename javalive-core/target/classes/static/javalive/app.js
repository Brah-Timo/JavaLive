import { createApp, defineComponent, ref, computed } from 'vue';
import { createRouter, createWebHistory } from 'vue-router';
import { initSocket, connectionStatus } from '/javalive/runtime.js';

// ── Lazy-load all generated page components ───────────────────────────────────
// The processor generates these files from @VuePage annotated classes.
// We try to load the router first; if it exists, use it.
// Otherwise fall back to a minimal hard-coded route set.

let router;

try {
  // Auto-generated router (only present if at least one @VuePage exists)
  const routerModule = await import('/javalive/router.js');
  router = routerModule.default || routerModule.router;
  if (router) {
    console.log('[JavaLive] ✅ Loaded generated router');
  }
} catch (_) {
  // No generated router — build one on the fly from the manifest
  console.log('[JavaLive] No generated router found; building dynamic router');
}

if (!router) {
  // Fallback: discover routes from manifest (generated by StateSchemaGenerator)
  let routes = [{ path: '/', redirect: '/dashboard' }];

  try {
    const resp = await fetch('/javalive/components/manifest.json');
    if (resp.ok) {
      const manifest = await resp.json();
      for (const entry of (manifest.pages || [])) {
        routes.push({
          path: entry.path,
          name: entry.name,
          component: () => import(`/javalive/components/${entry.componentName}.js`),
        });
      }
    }
  } catch (_) { /* ignore */ }

  router = createRouter({
    history: createWebHistory(),
    routes,
    scrollBehavior(to, from, saved) { return saved || { top: 0 }; },
  });
}

// ── Root application component ────────────────────────────────────────────────
const App = defineComponent({
  name: 'JavaLiveApp',

  setup() {
    const wsStatus  = connectionStatus;
    const isDot     = ref(false);

    const dotClass = computed(() => ({
      'jl-status-dot': true,
      'connected': wsStatus.value === 'connected',
    }));

    const statusText = computed(() => {
      switch (wsStatus.value) {
        case 'connected':    return 'Connected';
        case 'connecting':   return 'Connecting…';
        case 'reconnecting': return 'Reconnecting…';
        case 'error':        return 'Error';
        default:             return 'Disconnected';
      }
    });

    return { wsStatus, dotClass, statusText };
  },

  template: `
    <div class="jl-app">
      <nav class="jl-nav">
        <a href="/" class="jl-nav-brand">☕ JavaLive</a>
        <div class="jl-nav-links">
          <router-link to="/dashboard" class="jl-nav-link">📊 Dashboard</router-link>
          <router-link to="/users"     class="jl-nav-link">👥 Users</router-link>
          <a href="/javalive/status"   class="jl-nav-link" target="_blank">🔌 Status</a>
          <span style="font-size:0.7rem;color:#4ade80;padding:0 0.5rem">v0.1-alpha</span>
          <div style="display:flex;align-items:center;gap:5px;font-size:0.75rem;color:#64748b">
            <div :class="dotClass"></div>
            {{ statusText }}
          </div>
        </div>
      </nav>
      <main id="app-view">
        <router-view />
      </main>
    </div>
  `,
});

// ── Mount ─────────────────────────────────────────────────────────────────────
const app = createApp(App);
app.use(router);
app.mount('#app');

// ── Connect WebSocket (non-blocking; each component also calls initSocket) ────
initSocket('/javalive-ws').catch(err => {
  console.warn('[JavaLive] WebSocket connect failed:', err);
});

